const ACRONYM_UPPER = new Set(Object.values(GO_ACRONYMS).map((s) => s.toLowerCase()));

function splitCamel(s: string): string[] {
  return s
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1 $2")
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean);
}

function pascal(s: string): string {
  if (!s) return s;
  const segs = splitCamel(s);
  return segs
    .map((seg) => {
      const lower = seg.toLowerCase();
      if (ACRONYM_UPPER.has(lower)) return lower.toUpperCase();
      return lower[0].toUpperCase() + lower.slice(1);
    })
    .join("");
}

function goPrim(name: string): string {
  switch (name) {
    case "string":
    case "datetime":
    case "at-uri":
    case "uri":
    case "at-identifier":
    case "did":
    case "cid":
    case "cid-link":
      return "string";
    case "integer":
      return "int";
    case "boolean":
      return "bool";
    case "bytes":
      return "[]byte";
    case "blob":
      return "BlobRef";
    default:
      return "any";
  }
}

function goType(t: TypeRef, inArray = false): string {
  switch (t.kind) {
    case "primitive":
      return goPrim(t.name);
    case "array":
      return "[]" + goType(t.items, true);
    case "ref": {
      if (t.targetId === "unknown") return "any";
      return inArray ? t.targetId : "*" + t.targetId;
    }
    case "union":
      return "any";
    case "unknown":
      return "any";
  }
}

function jsonTag(name: string): string {
  return `\`json:"${name},omitempty"\``;
}

function emitStruct(t: NamedType): string {
  const lines: string[] = [];
  if (t.description) lines.push(`// ${t.name} ${t.description.replace(/\n/g, " ")}`);
  lines.push(`type ${t.name} struct {`);
  for (const f of t.fields) {
    if (f.description) lines.push(`\t// ${f.description.replace(/\n/g, " ")}`);
    const goName = pascal(f.name);
    const ty = goType(f.type);
    lines.push(`\t${goName} ${ty} ${jsonTag(f.name)}`);
  }
  lines.push("}");
  return lines.join("\n");
}

export function emitGo(reg: Registry): string {
  return [HEADER, ...reg.types.map(emitStruct)].join("\n\n") + "\n";
}
