function camelToSnake(s: string): string {
  return s.replace(/([A-Z])/g, "_$1").toLowerCase().replace(/^_/, "");
}

const RUST_KEYWORDS = new Set([
  "as", "break", "const", "continue", "crate", "else", "enum", "extern",
  "false", "fn", "for", "if", "impl", "in", "let", "loop", "match", "mod",
  "move", "mut", "pub", "ref", "return", "self", "Self", "static", "struct",
  "super", "trait", "true", "type", "unsafe", "use", "where", "while",
  "async", "await", "dyn", "abstract", "become", "box", "do", "final",
  "macro", "override", "priv", "typeof", "unsized", "virtual", "yield",
]);

function rustFieldName(camel: string): string {
  let snake = camelToSnake(camel);
  if (RUST_KEYWORDS.has(snake)) snake = snake + "_";
  return snake;
}

function rustPrim(name: string): string {
  switch (name) {
    case "string":
    case "at-uri":
    case "uri":
    case "at-identifier":
    case "did":
    case "cid":
    case "cid-link":
      return "String";
    case "datetime":
      return "DateTime<Utc>";
    case "integer":
      return "i64";
    case "boolean":
      return "bool";
    case "bytes":
      return "Vec<u8>";
    case "blob":
      return "BlobRef";
    default:
      return "Value";
  }
}

function rustType(t: TypeRef): string {
  switch (t.kind) {
    case "primitive":
      return rustPrim(t.name);
    case "array":
      return `Vec<${rustType(t.items)}>`;
    case "ref":
      return t.targetId === "unknown" ? "Value" : t.targetId;
    case "union":
      return "Value";
    case "unknown":
      return "Value";
  }
}

function emitStruct(t: NamedType): string {
  const lines: string[] = [];
  if (t.description) lines.push(`/// ${t.description.replace(/\n/g, " ")}`);
  lines.push(`#[derive(Debug, Clone, Serialize, Deserialize, Default)]`);
  lines.push(`pub struct ${t.name} {`);
  for (const f of t.fields) {
    if (f.description) lines.push(`    /// ${f.description.replace(/\n/g, " ")}`);
    const snake = rustFieldName(f.name);
    const renameNeeded = snake !== f.name;
    const ty = rustType(f.type);
    const isArray = f.type.kind === "array";
    if (f.required) {
      const tag = renameNeeded ? `    #[serde(rename = "${f.name}")]\n` : "";
      lines.push(`${tag}    pub ${snake}: ${ty},`);
    } else if (isArray) {
      const renamePart = renameNeeded ? `rename = "${f.name}", ` : "";
      lines.push(`    #[serde(${renamePart}default, skip_serializing_if = "Vec::is_empty")]`);
      lines.push(`    pub ${snake}: ${ty},`);
    } else {
      const renamePart = renameNeeded ? `rename = "${f.name}", ` : "";
      lines.push(`    #[serde(${renamePart}default, skip_serializing_if = "Option::is_none")]`);
      lines.push(`    pub ${snake}: Option<${ty}>,`);
    }
  }
  lines.push(`}`);
  return lines.join("\n");
}

export function emitRust(reg: Registry): string {
  return [HEADER, ...reg.types.map(emitStruct)].join("\n\n") + "\n";
}
