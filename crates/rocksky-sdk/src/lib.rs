//! # rocksky-sdk
//!
//! The official Rust SDK for [Rocksky](https://rocksky.app) â€” a music scrobbling
//! & discovery platform built on the [AT Protocol](https://atproto.com), on top
//! of [jacquard](https://crates.io/crates/jacquard).
//!
//! The design mirrors Bluesky's [`@atproto/api`](https://github.com/bluesky-social/atproto/tree/main/packages/api):
//! a [`RockskyAgent`] wraps a jacquard session and exposes both high-level
//! convenience verbs (`scrobble`, `like`, `follow`, `shout`, `set_now_playing`)
//! and a typed namespace escape hatch. Reads go through the unauthenticated
//! [`AppView`] client.
//!
//! ```no_run
//! # async fn demo() -> rocksky_sdk::Result<()> {
//! use rocksky_sdk::{RockskyAgent, ScrobbleDraft};
//!
//! let agent = RockskyAgent::builder()
//!     .session_store("~/.config/rocksky/session.json")
//!     .build()?;
//! agent.login_password("alice.bsky.social", "app-password").await?;
//!
//! agent
//!     .scrobble(&ScrobbleDraft {
//!         title: "Chaser".into(),
//!         artist: "Calibro 35".into(),
//!         album: "Jazzploitation".into(),
//!         album_artist: "Calibro 35".into(),
//!         duration_ms: 182_320,
//!         ..Default::default()
//!     })
//!     .await?;
//!
//! let recent = agent.appview().scrobbles("alice.bsky.social", 25, 0).await?;
//! # let _ = recent;
//! # Ok(())
//! # }
//! ```
//!
//! The typed `app.rocksky.*` records live in [`app_rocksky`] (also re-exported as
//! [`lexicons`]); they are generated by `jacquard-codegen` from
//! `apps/api/lexicons/` (see `scripts/gen-lexicons.sh`).

